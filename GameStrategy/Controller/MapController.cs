        public static void SetObject(int x, int y)
        {
            var posX = x / cellSize;
            var posY = y / cellSize;
            map[posY, posX] = 5;
        }

        public static void DrawMap(Graphics g, Castle castle)
        {
            for (var i = 0; i < MapWith; i++)
            for (var j = 0; j < MapHeight; j++)
            {
                if (map[i, j] == 1 || map[i, j] == 5)
                    g.DrawImage(sprite,
                        new Rectangle(new Point(j * cellSize, i * cellSize), new Size(cellSize, cellSize)), 448, 128,
                        60, 60, GraphicsUnit.Pixel);
                if (map[i, j] == 2)
                    g.DrawImage(sprite,
                        new Rectangle(new Point(j * cellSize, i * cellSize), new Size(cellSize, cellSize)), 516, 443,
                        60, 70, GraphicsUnit.Pixel);
            }
        }

        public static void DrawBorderCellOnClick(int posX, int posY, Graphics g)
        {
            InteractionPosX = posX / cellSize;
            InteractionPosY = posY / cellSize;
            var posBorderX = posX / cellSize * cellSize;
            var posBorderY = posY / cellSize * cellSize;
            var pen = new Pen(Color.Gold, 2);
            g.DrawRectangle(pen, posBorderX, posBorderY, cellSize, cellSize);
        }
